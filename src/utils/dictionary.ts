// Sanitize the list once at runtime to ensure no hidden whitespace and NO DUPLICATES
export const COMMON_WORDS: string[] = Array.from(new Set(RAW_WORDS.map(w => w.trim().toUpperCase())));

export const DICTIONARY = new Set(COMMON_WORDS);

/**
 * Check if a word can be built from a pool of letters (respecting character counts)
 */
export const isSubAnagram = (word: string, letters: string[]): boolean => {
  const pool = [...letters];
  for (const char of word) {
    const idx = pool.indexOf(char);
    if (idx === -1) return false;
    pool.splice(idx, 1);
  }
  return true;
};

/**
 * Return all COMMON_WORDS that can be spelled from given letters (respecting counts)
 */
export const getBonusWords = (letters: string[]): string[] => {
  const upper = letters.map(l => l.toUpperCase());
  return COMMON_WORDS.filter(word =>
    word.length >= 3 && isSubAnagram(word, upper)
  );
};
